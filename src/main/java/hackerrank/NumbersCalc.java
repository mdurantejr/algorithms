package hackerrank;

public class NumbersCalc {

    public static void main(String[] args) {
        int a = 1530;
        System.out.println(a % 10); // 0
        System.out.println(a % 100); // 30

        int b = 1;
        System.out.println(b % 10); // 0
        System.out.println(b % 100); // 0
    }

}
